#include "StringCalculator.h"

int StringCalculator::add(const std::string& numbers) {
    // If the input string is empty, return 0
    if (numbers.empty()) {
        return 0;
    }
    return 0; 
}

int StringCalculator::add(const std::string& numbers) {
    if (numbers == "0") {
        return 0;
    }
    return 0; // Default return value for simplicity, can be extended for more cases
}
